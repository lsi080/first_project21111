import streamlit as st

st.title("📚 나에게 맞는 공부 방법 추천기")

st.write("""
아래 질문에 답해주시면,  
당신의 성향에 맞는 공부 방법과 계획을 추천해드립니다!
""")

# 1. 질문 - 학습 스타일
learning_style = st.radio(
    "어떤 방식으로 공부하는 걸 더 선호하나요?",
    ("읽고 쓰면서 공부하기", "듣고 말하면서 공부하기", "직접 해보면서 배우기")
)

# 2. 질문 - 집중 시간
focus_time = st.selectbox(
    "한 번에 집중할 수 있는 시간은 얼마나 되나요?",
    ("15분 이하", "30분", "1시간 이상")
)

# 3. 질문 - 계획 선호도
plan_type = st.radio(
    "공부 계획을 어떻게 짜는 걸 좋아하나요?",
    ("꼼꼼하게 세세하게", "큰 틀만 잡아서 유연하게", "즉흥적으로 그때그때")
)

# 추천 버튼
if st.button("내 성향에 맞는 공부법 추천받기"):

    st.markdown("### 당신의 공부 성향 분석 결과:")

    # 학습 스타일별 추천
    if learning_style == "읽고 쓰면서 공부하기":
        method = "노트 필기와 요약, 그리고 책 읽기를 중점적으로 하세요."
    elif learning_style == "듣고 말하면서 공부하기":
        method = "강의 듣기, 스터디 그룹에서 말하기, 음성 녹음 활용을 추천합니다."
    else:
        method = "실습과 문제 풀이 중심으로 적극 참여하는 공부법이 효과적입니다."

    # 집중 시간별 추천
    if focus_time == "15분 이하":
        focus_tip = "짧은 시간 집중 후 자주 휴식을 가지며, 포모도로 기법을 활용해보세요."
    elif focus_time == "30분":
        focus_tip = "30분 집중 후 5분 휴식, 4번 반복하는 루틴이 적합합니다."
    else:
        focus_tip = "1시간 이상 꾸준히 집중하되 중간에 스트레칭이나 가벼운 운동을 권장합니다."

    # 계획 선호도별 추천
    if plan_type == "꼼꼼하게 세세하게":
        plan_tip = "매일 할 일을 구체적으로 정하고, 목표 달성 여부를 체크하세요."
    elif plan_type == "큰 틀만 잡아서 유연하게":
        plan_tip = "주간 계획만 세우고, 상황에 맞게 조정하는 방식을 추천합니다."
    else:
        plan_tip = "그날그날 집중할 내용을 정하고, 융통성 있게 공부하세요."

    # 결과 출력
    st.write(f"**학습 스타일:** {method}")
    st.write(f"**집중 시간:** {focus_tip}")
    st.write(f"**공부 계획:** {plan_tip}")
